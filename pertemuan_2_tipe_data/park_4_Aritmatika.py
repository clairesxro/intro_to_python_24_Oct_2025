x = 3
y = 2

# penjumlahan
hasil = x + y 
hasil_penjumlahan = hasil

#hasil = "Rp." + str(hasil) #string concenate atau menyambungkan string
print (f"Hasil penjumlahan dari {x} + {y} = {hasil}")

# pengurangan
hasil = x - y 
print (f"Hasil penjumlahan dari {x} - {y} = {hasil}")

# perkalian 
hasil = x * y
print (f"Hasil penjumlahan dari {x} * {y} = {hasil}")

# pembagian
hasil = x // y
print (f"Hasil penjumlahan dari {x} // {y} = {hasil}")

# modulus
hasil = x % y
print (f"Hasil penjumlahan dari {x} % {y} = {hasil}")

# incerment
# hasil = hasil + 6
hasil_penjumlahan += 6
print(f"hasil akhir {hasil_penjumlahan}")

# decerment
hasil_penjumlahan -= 6
print (f"hasil akhir {hasil_penjumlahan}")